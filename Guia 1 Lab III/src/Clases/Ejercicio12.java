package Clases;

public class Ejercicio12 {
	
	
	public Ejercicio12() {
	    super();
	
	}
	
	public int areaTriangulo(int base,int altura)
	{
	    int area;
	
	    area=(base*altura)/2;
	
	    return area;
	
	
	}
	
	public int areaCuadrado(int base)
	{
	    int area;
	
	    area=base*4;
	
	    return area;
	
	}
	
	public double areaCirculo(double radio)
	{
	    double area;
	
	    area=Math.PI*(radio*radio);
	
	    return area;
	}
	
	public int areaRectangulo(int a,int b)
	{
	    int area;
	
	    area=a*b;
	
	    return area;
	
	
	}
	
	
}
